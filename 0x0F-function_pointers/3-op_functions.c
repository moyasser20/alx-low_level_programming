#include "3-calc.h"

/**
 * op_add - ss
 *
 * @a: dd
 * @b: ss
 * Return: sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - ss
 *
 * @a: dd
 * @b: ss
 * Return: subtruct
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - ss
 *
 * @a: dd
 * @b: ss
 * Return: mulltiplication
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - ss
 *
 * @a: dd
 * @b: ss
 * Return: div
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - ss
 *
 * @a: dd
 * @b: ss
 * Return: mod
*/
int op_mod(int a, int b)
{
return (a % b);
}
